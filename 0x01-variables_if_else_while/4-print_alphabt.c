#include <stdio.h>

/**
 * main - Entry point
 * Description - Alphabet soup
 * Return: 0
 * */
int main(void)
{
	char c;

	for (c = 'a'; c <= 'z'; c++)
	{
		if (c != 'e' && c != 'q')
		{
			putchar(c);
		}
	}
	putchar('\n');

	return (0);
}#include <stdio.h>

